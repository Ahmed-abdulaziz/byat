المعلومات الشخصية التي نجمعها منك
<br>
1- عند تسجيلك على التطبيق أو الموقع، سوف يُطلب منك أن تقدم لنا بعض المعلومات الشخصية مثل اسمك وعنوانك وعنوان البريد الإلكتروني الخاص بك و رقم الهاتف ومعلومات أخرى مشابهة، وكذلك بعض المعلومات الإضافية عنك مثل تاريخ ميلادك أو معلومات تعريفية أخرى.
بالإضافة إلى ذلك، نحتاج للتحقق الأمني، وللتأكد من عدم كونك تنتحل شخصية شخص آخر، ربما نتحقق من رقم هاتفك وعنوان البريد الالكتروني الخاص بك.
<br>2- بعد تسجيلك في التطبيق أو الموقع، لا تشارك اي معلومات شخصية (بما في ذلك أية معلومات مالية) في أي مكان في التطبيق و الموقع، ما عدا قسم إعدادات الحساب الشخصي. تقييد مشاركة المعلومات الشخصية في كافة الأقسام ما عدا قسم إعدادات الحساب الشخصي، يحميك من إمكانية تزوير أو سرقة الهوية. ونشر أي معلومات شخصية من قبلك في أي مكان في التطبيق أو الموقع قد يؤدي إلى تعليق حسابك.
<br>3- نحن نجمع معلومات المعاملات المبنية على أساس الأنشطة الخاصة بك باستخدام مزاد شركة لايطوفك (مثل عمليات شراء وبيع الأشياء)، وتستخدم هذه المعلومات فقط فيما يتعلق بالمعاملات التي تجري على مزاد شركة لايطوفك وليس لأي أسباب أخرى.
<br>4- يرجى ملاحظة أننا قد نستخدم عنوان بروتوكول الانترنت الخاص بك(IP) (وهو رقم فريد خاص بجهازك الالكتروني أو مزود خدمة الإنترنت (ISP)) لتحليل اتجاهات المستخدمين وتحسين إدارة مزاد شركة لايطوفك .
<br>
<br>5- يجوز لنا أيضا جمع معلومات عن الجهاز الالكتروني الخاص بك (على سبيل المثال، نوع الجهاز، ونوع المتصفح) والمعلومات الملاحية (على سبيل المثال، الصفحات التي تزورها على مزاد شركة لايطوفك ) بالتزامن مع استخدامك مزاد شركة لايطوفك .
تطبيق مزاد شركة لايطوفك مخوّل بالوصول لكاميرا جهازك الالكتروني والاستوديو الخاص به، عند إضافتك صور للإعلان عن عنصر جديد، أو لتعديل صور إعلان سابق. وأخيرا، نجمع معلومات إضافية منك أو عنك بطرق أخرى غير مذكورة هنا بالتحديد.
على سبيل المثال: قد نجمع معلومات خاصة بتواصلك مع فريق دعم العملاء لدينا، أو نتائج اجاباتك على استفسار أو استفتاء، ويجوز لنا أيضا جمع ردود الفعل الخاصة بك، والتقييمات والتعليقات الأخرى التي تتعلق باستخدامك مزاد شركة لايطوفك .
حيث إننا نجمع المعلومات الشخصية لأغراض إحصائية، ويجب أن تبقى مثل هذه المعلومات الشخصية مجهولة الهوية.

استخدامنا لمعلوماتك الشخصية

1- نحن نستخدم المعلومات الشخصية الخاصة بك لتوفير الخدمات ودعم العملاء لك؛ ولقياس وتحسين الخدمات التي نقدمها لك؛ ولمنع الأنشطة غير المشروعة وتنفيذ الاتفاق المستعمل معكم ( "اتفاقية المستخدم")؛ ولاستكشاف المشاكل التقنية؛ وفي جمع الرسوم؛ كما نستخدمها في إرسال الرسائل الترويجية لك، وفي التحقق من المعلومات التي تقدمها لنا مع أطراف ثالثة. على سبيل المثال: قد نشارك بعض المعلومات الشخصية التي تقدمها لنا مع خدمات التحقق مع أطراف ثالثة للتأكد من عمليات الاحتيال.

2- على الرغم من بذلنا كل جهد ممكن للحفاظ على خصوصيتك، إلا أننا قد نضطر إلى الكشف عن المعلومات الشخصية الخاصة بك إلى هيئات تنفيذ القانون والوكالات الحكومية أو أطراف ثالثة أخرى، حيث نضطر للقيام بذلك بأمر قضائي أو إجراء قانوني مماثل؛ حيث أننا مطالبون بالكشف عن معلوماتك الشخصية للتوافق مع القانون والتعاون مع أي تحقيق جاري إنفاذاً للقانون، واعتقادنا بأن الكشف عن المعلومات الشخصية الخاصة بك ضرورة لمنع أي أذى جسدي أو خسارة مالية، أو للتبليغ عن نشاط غير قانوني يشتبه به أو للتحقيق في انتهاك محتمل لاتفاقية المستخدم لدينا.
<br>
<br>3- في حالة بيع مزاد شركة لايطوفك ، أو أي من الفروع والشركات التابعة أو أي الأصول التجارية ذات الصلة، يمكن الكشف عن المعلومات الشخصية الخاصة بك إلى أي مشتر محتمل بهدف استمرار التقدّم، أو فيما يتعلق بأي عملية بيع أخرى.
قد نشارك معلوماتك الشخصية مع شركائنا في مجموعة شركات أخرى وذلك لتوفير المحتوى والخدمات المشتركة لكم، وللمساعدة في الكشف عن الأعمال غير المشروعة و / أو أي انتهاكات لسياساتنا. يجوز لنا أيضا مشاركة المعلومات الشخصية مع الاعضاء الاخرين في الموقع كجزء من مزاد أو عمليات الشراء على الموقع. على سبيل المثال، في نهاية مزاد ناجح على الموقع، سنعرض للبائع والمشتري معلومات الاتصال ببعضهم.
<br>
وستكون هذه المعلومات مبنية على المعلومات الشخصية التي قدمتها لنا عند التسجيل، ولذلك يجب أن تستمر في تحديث معلوماتك الشخصية. 4-
 بالإضافة إلى ذلك، يتم عرض المعلومات المتعلقة بالعناصر التي تقوم ببيعها وشرائها على التطبيق والموقع. ويمكن أن تشمل هذه المعلومات تفاصيل عن هوية المستخدم، رقم هاتفك، عنوانك، وتقييم ردود الفعل والملاحظات المتعلقة باستخدامك التطبيق والموقع.
<br>
5- نحن لا نقوم بعملية بيع أو تأجير أي من معلوماتك الشخصية إلى أطراف ثالثة في السياق الطبيعي لممارسة الأعمال التجارية ولن نشارك معلوماتك الشخصية مع أطراف ثالثة وفقاً لسياسة الخصوصية هذه.
<br>
6- من خلال التسجيل في التطبيق أو الموقع، فإنك توافق موافقة صريحة على تلقي رسائل إلكترونية ترويجية ورسائل حول خدماتنا ورسائل إلكترونية بخصوص التغييرات، والميزات الجديدة على مزاد شركة لايطوفك .

7- بالإضافة إلى ذلك، نستخدم التعليقات التي أدليت بها حول مزاد شركة لايطوفك لأغراض التسويق، وبكتابتك لهذه التعليقات فأنت توافق موافقة صريحة على استخدامنا مثل هذه التعليقات لأغراض التسويق.

استخدامك للمعلومات الشخصية الخاصة بك وبالمستخدمين الآخرين

1- قد يحتاج الأعضاء لتبادل المعلومات الشخصية مع بعضهم البعض لإكمال المعاملات على مزاد شركة لايطوفك . لذلك يجب عليك احترام خصوصية بقية الأعضاء في جميع الأوقات.
2- لا يمكن لنا أن نضمن خصوصية المعلومات الشخصية الخاصة بك عند مشاركتك المعلومات الشخصية مع الأعضاء الآخرين، لذلك يجب عليك الاطلاع باستمرار على معلومات عن سياسات الخصوصية والأمن الخاصة بالأعضاء الآخرين الذين تتعامل معهم قبل مشاركة أي من معلوماتك الشخصية مع عضو آخر.
3- لا تشمل سياسة الخصوصية هذه كشفك لأي من معلوماتك الشخصية لعضو آخر.
4- إنك توافق على استخدام أي من المعلومات الشخصية التي وردت من أعضاء موقع آخر فيما يتعلق بمعاملة في الموقع فقط، ومرتبطة بهذه الصفقة، ويجب عدم استخدام المعلومات الواردة من أعضاء موقع آخر لأية أغراض أخرى (إلا بموافقة صريحة من عضو موقع آخر).
<br>5- أنت تقر وتوافق بأنك لن تستخدم المعلومات الشخصية التي وردت من أعضاء موقع آخر وفقاً لجميع القوانين المعمول بها.

الوصول والمراجعة والتعديل للمعلومات الشخصية الخاصة بك
<br>
1- يمكنك الوصول للمعلومات الشخصية الخاصة بك ومراجعتها في قسم "حسابي" في التطبيق والموقع.
إذا حدث أي تغيير على المعلومات الشخصية الخاصة بك بأي شكل من الأشكال أو تم تقديمها بشكل غير صحيح على مزاد شركة لايطوفك يجب عليك فوراً تحديث أو تصحيح المعلومات الشخصية الخاصة بك (حسب الحاجة) عن طريق الوصول إلى قسم حسابي في مزاد شركة لايطوفك أو من خلال الاتصال بفريق خدمة العملاء لدينا.
رابط "اتصل بنا" في أعلى كل صفحة من صفحات الموقع يحتوي على البريد الإلكتروني والهاتف الخاص بدعم العملاء.
<br>2- يرجى ملاحظة أننا يجب أن نحتفظ بالمعلومات الشخصية الخاصة بك أثناء وبعد الانتهاء من استخدامك لمزاد شركة لايطوفك كما هو مطلوب للامتثال للقانون، ولاكتشاف المشاكل التقنية وإصلاحها، ولمنع الغش والتزوير، وللمساعدة في أي تحقيق قانوني، ولاتخاذ اي اجراءات اخرى غير ذلك يسمح بها القانون.
ارتباطات موقع طرف ثالث
ربما يحتوي مزاد شركة لايطوفك على روابط لمواقع أخرى. ونحن غير مسؤولين عن قوانين وسياسات الخصوصية لتلك المواقع. لذلك ننصحك عند مغادرة مزاد شركة لايطوفك بقراءة بيانات الخصوصية لكل موقع تزوره في حال كنت تنوي تقديم معلومات شخصية لهذا الموقع. تنطبق سياسة الخصوصية الخاصة بنا على المعلومات الشخصية التي نجمعها على مزاد شركة لايطوفك .
الكوكيز 
<br>1- يستخدم مزاد شركة لايطوفك تقنية الكوكيز مثله مثل العديد من المواقع والتطبيقات المشابهة، وهي (ملفات صغيرة توضع على الجهاز الخاص بك) عند زيارتك لصفحات وأقسام معينة، وهي تقنية لتعريف متصفحك برقم عشوائي فريد.
<br>2- ملفات الكوكيز التي نستخدمها لا تكشف أي معلومات شخصية عنك. إنما تساعدنا في متابعة وتحسين تجربتك عبر مزاد شركة لايطوفك . وأيضا تساهم في متابعتنا أي الأقسام هي الاكثر شهرة.
لديك دائماً الحرية في رفض استخدام تقنية الكوكيز إذا سمح متصفحك بذلك، على الرغم من أن قيامك بذلك يؤدي لتعارض مع استخدام مزاد شركة لايطوفك .
<br>
