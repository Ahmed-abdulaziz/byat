2- لا تشكّل المعلومات مجهولة المصدر معلومات شخصية، لأنه لا يمكن استخدامها لتحديد أي شخص، لذا نجمع المعلومات الشخصية عند استخدام تطبيق مزاد شركة لايطوفك ، أو المواقع والخدمات ذات الصلة به (بما في ذلك دون حصر، عند التسجيل، والشراء والبيع والتبادل و المشاركة في المزادات، أو عند التواصل مع فريق دعم العملاء الخاص بنا).
