ومن خلال تزويدنا بالمعلومات الشخصية الخاصة بك، فإنك توافق صراحة بحقنا في معالجة المعلومات الشخصية الخاصة بك وفقا لشروط سياسة الخصوصية.
<br>3- يحقّ لنا تعديل سياسة الخصوصية في أي وقت عن طريق نشر النسخة المعدلة في الموقع الالكتروني الخاص بشركة لايطوفك ، وتكون سياسة الخصوصية المعدلة سارية المفعول من تاريخ نشرها، ويُعتبر استمرارك في استخدام تطبيق شركة لايطوفك والمواقع الالكترونية الخاصة به وبقية الخدمات بمثابة موافقة صريحة منك بحقنا بالاستمرار في معالجة المعلومات الشخصية الخاصة بك وفقا لشروط سياسة الخصوصية المعدلة.
